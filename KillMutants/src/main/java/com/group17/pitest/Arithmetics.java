package com.group17.pitest;

class Arithmetics {


    public int sum(int a, int b) {
        int sum = a+b;
        return sum;
    }

    public int difference(int a, int b) {
        int different = a-b;
        return different;
    }

    public int product(int a, int b) {
        int product = a*b;
        return product;
    }

    public int quot(int a, int b) {
        int quot = a/b;
        return quot;
    }

  

}
